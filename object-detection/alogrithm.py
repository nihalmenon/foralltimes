def display(frame):
    mlx_shape = (24,32)
    #the code below is based on a online tutorial detailing how to create an image using the thermal data
    plt.ion()
    fig,ax = plt.subplots(figsize=(12,7))
    therm1 = ax.imshow(np.zeros(mlx_shape),vmin=0,vmax=60) #start plot with zeros
    cbar = fig.colorbar(therm1) # setup colorbar for temps
    cbar.set_label('Temperature [$^{\circ}$C]',fontsize=14) # colorbar label
    therm1.set_data(np.fliplr(frame)) # flip left to right
    therm1.set_clim(vmin=np.min(frame),vmax=np.max(frame)) # set bounds
    plt.pause(5) # required
    plt.show()


