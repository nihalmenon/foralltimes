class Aws(Scene):
    def construct(self): 
        pi = ImageMobject("pi.png").scale(0.5)
        aws = ImageMobject("aws.png").scale(0.6)
        jeff = ImageMobject("jeff.jpeg")
        amazon = Group()
        amazon.add(aws, jeff)
        self.play(FadeIn(pi))
        self.play(pi.animate.scale(0.3).shift(LEFT * 5))
        self.play(FadeIn(amazon))
        self.play(amazon.animate.scale(0.3).shift(RIGHT * 5))
        present = ImageMobject("present.png")
        present.shift(LEFT*3).scale(0.25)
        self.add(present)
        self.wait(0.5)
        self.play(present.animate.shift(5 * RIGHT))
        self.play(jeff.animate.scale(1.5))
        self.wait(0.5)

